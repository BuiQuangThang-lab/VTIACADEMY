
public class Employee {
  int id;
  String Ename;
  public Employee() {	
  }
  
  public Employee(int id, String Ename) {
	  this.id = id;
	  this.Ename = Ename;
  }

public int getId() {
	return id;
}

public void setId(int id) {
	this.id = id;
}

public String getEname() {
	return Ename;
}

public void setEname(String ename) {
	Ename = ename;
}
}

